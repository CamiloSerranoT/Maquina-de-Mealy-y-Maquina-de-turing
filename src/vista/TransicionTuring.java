    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() == jb1) {
            control.explicaciones(3);
        } else {
            if (e.getSource() == jb2) {
                String aux = "";
                if (jComboBox1.getSelectedIndex() == 0) {
                    aux = aux + "L";
                    control.transicionTuring(jt1.getText(), jt2.getText(), jt3.getText(), jt4.getText(), aux);
                } else {
                    aux = aux + "R";
                    control.transicionTuring(jt1.getText(), jt2.getText(), jt3.getText(), jt4.getText(), aux);
                }
            } else {
                if (e.getSource() == jb3) {
                    control.eliminarTransicionMT(jTable1);
                } else {
                    control.pasoDeClases(4);
                }
            }
        }
    }

